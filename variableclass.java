public class variableclass {
    public static void main(String args[]){
        int num=23;//int stores integer value in the num variable; here num variable is assigned 23.
        float point=3.14f;//float stores floating point numbers; here point variable is assigned 3.14f; 'f' is written after point value.
        char d='b';//char stores charecter values; here d stroes character value 'b'; d is assigned 'b';
        String s="My Name Is Bidhan";//String stores "sequence of character"; S--> in String S is in uppercase.
        boolean b=true;//boolean variable stores true and false.
        System.out.println(num+" Is an integer.");
        System.out.println(point+" Is a Floating Point Number.");
        System.out.println(d+" Is a Character.");
        System.out.println(s+" Is a String .");
        System.out.println(b+" Is Boolean.");
    }
}